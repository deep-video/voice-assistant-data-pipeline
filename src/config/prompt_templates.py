# 表达种子生成提示词

SEED_GENERATION_PROMPT = """
你是语言学家，需要为"{intent}"意图收集真实用户可能说的话。

**意图描述**：{intent_description}

**核心要求**：
1. 像真人对车载语音助手说话，内容精炼
2. 口语化，可以有语气词、省略、方言、网络用语

**种子类型要求**（严格控制比例）：
- 直接请求型：约30%，明确表达需求
- 情绪驱动型：约25%，情绪词+请求
- 场景触发型：约25%，场景词+请求
- 模糊需求型：约20%，不明确的请求

生成恰好 {num_seeds} 条种子，输出 JSON 数组：
[
  {{"text": "种子1", "type": "direct_request"}},
  ...
]

类型：direct_request / emotion_driven / scene_triggered / vague_request

**约束**：
- 只输出 JSON，不要其他内容
- 单句能判断意图，不依赖外部上下文
- 提及历史时必须明确实体（如"上次推荐的歌"）
- 用户是在请求{intent_action}，而非{intent_negative_example}
{intent_constraints}
- "推荐"一词最多用30%，其余用"有啥"、"适合什么"、"给点"、"随便"、"看着办"等替代表达
- 句式多样化，不要同一模板
- 真实自然，不要文艺腔
"""

# 种子扩展提示词

SEED_EXPANSION_PROMPT = """
你是语言学家，需要基于给定的种子表达，生成更多真实用户可能说的变体。

**种子表达**：{seed_text}
**意图**：{intent}
**意图描述**：{intent_description}
**意图边界**：用户是在请求{intent_action}，而非{intent_negative_example}

**核心要求**：
1. 保持种子表达的核心意图不变
2. 生成恰好 {num_expansions} 条变体
3. **句式变换**（核心，占60%）：
   每条变体必须使用不同的句式结构，示例：
   - 疑问句："有啥能听的没？"
   - 祈使句："赶紧推几首！"
   - 陈述句："这会儿想听点歌"
   - 感叹句："堵死了！快给点音乐！"
   - 倒装句："听啥啊，这破路况"
   - 碎片句："堵车...音乐...推荐"
   - 省略句："推荐点，现在"
   
   **注意**：不同句式必须使用不同的词汇和结构，不要只是换句型。

4. **口语噪声**（占20%，必须出现）：
   - 语气词/填充词："嗯...有啥能听的？"
   - 结巴/重复："推、推荐点歌"
   - 中途改口："推荐点...算了给点建议"
   - 错别字："有啥适合听地？"
   - 标点混乱："有啥听的！！！"

5. **省略程度**（占20%）：
   - 完整句 → 省略句 → 碎片句

6. **限制重复**：
   - 禁止相同句式结构出现超过 {num_expansions}÷3 次
   - 禁止相同开头词出现超过 2 次
   - 禁止使用固定结构超过 3 次

7. 每条变体 5-20 字，像日常说话

8. **禁止**：
   - 诗意/文艺/抽象/比喻表达
   - "适合听进XX里/时"的句式
   - 超过20字或不足5字
   - 像诗歌或散文

**约束**：
- 只输出 JSON 数组，不要其他内容
- 单句能判断意图，不依赖外部上下文
{expansion_constraints}

输出格式：
["变体1", "变体2", ...]
"""

